def persona_instruction_for_title(title: str | None) -> str | None:
    """未知标题时返回 None，调用方仅附加已知人设。"""
    if not title or not str(title).strip():
        return None
    t = str(title).strip()
    return _PERSONA_BY_TITLE.get(t)


def compose_system_with_persona(
    base_system: str,
    *,
    ai_persona_title: str | None = None,
    ai_custom_instruction: str | None = None,
) -> str:
    """
    在智能体原有 system 后追加【用户个性化】块。无有效参数时返回 base_system。
    """
    parts: list[str] = [base_system.rstrip()]
    persona_line = persona_instruction_for_title(ai_persona_title)
    custom = (ai_custom_instruction or "").strip()

    if not persona_line and not custom:
        return base_system

    block_lines: list[str] = ["", "---", "【用户个性化】"]
    if persona_line and ai_persona_title:
        block_lines.append(
            f"用户选择的人设「{ai_persona_title.strip()}」。请严格遵守以下语气与风格（不得违背上方业务规则与事实要求）："
        )
        block_lines.extend(persona_line.splitlines())
    if custom:
        block_lines.append("用户补充说明（请务必遵守）：")
        block_lines.append(custom)

    parts.append("\n".join(block_lines))
    return "\n".join(parts).strip() + "\n"
